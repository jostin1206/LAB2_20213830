package com.example.lab120213830;

public class Configuracion {

    private String tema;
    private int longitudPalabra;
    private int numeroMaxIntentos;


    public String getTema() {
        return tema;
    }

    public void setTema(String tema) {
        this.tema = tema;
    }

    public int getLongitudPalabra() {
        return longitudPalabra;
    }

    public void setLongitudPalabra(int longitudPalabra) {
        this.longitudPalabra = longitudPalabra;
    }

    public int getNumeroMaxIntentos() {
        return numeroMaxIntentos;
    }

    public void setNumeroMaxIntentos(int numeroMaxIntentos) {
        this.numeroMaxIntentos = numeroMaxIntentos;
    }
}
